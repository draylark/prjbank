export async function getLinkTokenForRevalidation(accessToken: string) {
  try {
    const response = await plaidClient.linkTokenCreate({
      user: { client_user_id: "unique-user-id" },
      client_name: "Tu App",
      country_codes: [CountryCode.Us],
      language: 'es',
      access_token: accessToken, // Utiliza el accessToken específico del banco
      update: {},
    });
    return response.data.link_token; // Retorna el link_token generado
  } catch (error) {
    console.error('Error generando link_token para revalidación:', error);
    throw new Error('Error al generar el link_token');
  }
}


export const getBanksForRevalidation = async (banks: Bank[]) => {
  const banksReval = [];

  for (const bank of banks) {
    try {
      // Verificar cada token de acceso con Plaid
      await plaidClient.accountsGet({
        access_token: bank.accessToken,
      });
    } catch (error) {
      if (error.response?.data.error_code === 'ITEM_LOGIN_REQUIRED') {
        // Solo añadir los campos necesarios para la revalidación
        banksReval.push({
          accessToken: bank.accessToken,
          bankId: bank.bankId,
          id: bank.$id,
        });
      }
    }
  }

  return banksReval;
};


